lines.push("            <option>Marketing Agency</option>");
lines.push("            <option>Other</option>");
lines.push("          </select>");
lines.push("        </div>");
lines.push("        <div style={{ marginBottom: '32px' }}>");
lines.push("          <label style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '15px', fontWeight: 600, display: 'block', marginBottom: '8px' }}>City and State</label>");
lines.push("          <input type='text' placeholder='e.g. Tampa, FL' value={city} onChange={e => setCity(e.target.value)} style={{ width: '100%', padding: '14px 16px', fontSize: '16px', border: '2.5px solid #111', outline: 'none', fontFamily: 'DM Sans, sans-serif', boxSizing: 'border-box' }} />");
lines.push("        </div>");
lines.push("        <div style={{ marginBottom: '48px' }}>");
lines.push("          <label style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '15px', fontWeight: 600, display: 'block', marginBottom: '8px' }}>Website URL (optional)</label>");
lines.push("          <input type='text' placeholder='e.g. https://www.yourbusiness.com' value={website} onChange={e => setWebsite(e.target.value)} style={{ width: '100%', padding: '14px 16px', fontSize: '16px', border: '2.5px solid #111', outline: 'none', fontFamily: 'DM Sans, sans-serif', boxSizing: 'border-box' }} />");
lines.push("        </div>");
lines.push("        {error && <p style={{ color: 'red', marginBottom: '16px', fontFamily: 'DM Sans, sans-serif' }}>{error}</p>}");
lines.push("        <button onClick={handleSubmit} disabled={loading} style={{ display: 'block', width: '100%', background: loading ? '#ccc' : '#E8610A', color: '#fff', padding: '20px', fontSize: '18px', fontWeight: 700, border: 'none', cursor: loading ? 'not-allowed' : 'pointer', fontFamily: 'DM Sans, sans-serif', textAlign: 'center', boxSizing: 'border-box' }}>");
