const fs = require('fs');
const path = require('path');

// Create the directory
const dir = path.join(__dirname, 'src', 'app', 'dashboard', 'blog');
fs.mkdirSync(dir, { recursive: true });

// Write layout.tsx
const layout = [];
layout.push("// @ts-nocheck");
layout.push("'use client'");
layout.push("export default function Layout({ children }) {");
layout.push("  return <>{children}</>");
layout.push("}");
fs.writeFileSync(path.join(dir, 'layout.tsx'), layout.join('\n'), 'utf8');

// Write page.tsx
const lines = [];
lines.push("// @ts-nocheck");
lines.push("'use client'");
lines.push("");
lines.push("import { useState } from 'react'");
lines.push("import Nav from '@/components/Nav'");
lines.push("import Footer from '@/components/Footer'");
lines.push("import { createClient } from '@supabase/supabase-js'");
lines.push("");
lines.push("const supabase = createClient(");
lines.push("  process.env.NEXT_PUBLIC_SUPABASE_URL,");
lines.push("  process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY");
lines.push(")");
lines.push("");
lines.push("export default function BlogGenerator() {");
lines.push("  const [topic, setTopic] = useState('')");
lines.push("  const [tone, setTone] = useState('Professional')");
lines.push("  const [loading, setLoading] = useState(false)");
lines.push("  const [post, setPost] = useState(null)");
lines.push("  const [saved, setSaved] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("  const [copied, setCopied] = useState(false)");
lines.push("");
lines.push("  async function generatePost() {");
lines.push("    if (!topic) { setError('Please enter a topic or keyword.'); return }");
lines.push("    setLoading(true)");
lines.push("    setError('')");
lines.push("    setPost(null)");
lines.push("    setSaved(false)");
lines.push("    try {");
lines.push("      const { data: { user } } = await supabase.auth.getUser()");
lines.push("      const { data: profile } = await supabase");
lines.push("        .from('business_profiles')");
lines.push("        .select('business_name, industry, phone')");
lines.push("        .eq('user_id', user.id)");
lines.push("        .single()");
lines.push("      const businessName = profile?.business_name || 'our business'");
lines.push("      const industry = profile?.industry || 'local business'");
lines.push("      const city = profile?.phone || 'your city'");
lines.push("");
lines.push("      const prompt = `You are an expert SEO content writer and AI engine optimization specialist. Write a state-of-the-art blog post for a ${industry} business called ${businessName} located in ${city}.`");
lines.push("        + ` Topic: ${topic}. Tone: ${tone}.`");
lines.push("        + ` Requirements:`");
lines.push("        + ` 1. SEO-optimized title with primary keyword`");
lines.push("        + ` 2. Meta description under 160 characters`");
lines.push("        + ` 3. Introduction that hooks the reader and mentions ${city} naturally`");
lines.push("        + ` 4. 4-5 sections with H2 headings containing keywords`");
lines.push("        + ` 5. Naturally mention ${businessName} and ${city} throughout`");
lines.push("        + ` 6. FAQ section with 3 questions and answers (helps Google featured snippets and AI engine citations)`");
lines.push("        + ` 7. Strong call to action at the end mentioning ${businessName}`");
lines.push("        + ` 8. 1000-1500 words total`");
lines.push("        + ` 9. Written so AI engines like ChatGPT and Perplexity will cite this business when users ask about ${industry} in ${city}`");
lines.push("        + ` Format the response as JSON with these fields: title, metaDescription, content (full HTML blog post with proper h1 h2 h3 p tags), wordCount`");
lines.push("        + ` Return ONLY valid JSON, no markdown, no backticks.`");
lines.push("");
lines.push("      const response = await fetch('https://api.anthropic.com/v1/messages', {");
lines.push("        method: 'POST',");
lines.push("        headers: { 'Content-Type': 'application/json' },");
lines.push("        body: JSON.stringify({");
lines.push("          model: 'claude-sonnet-4-20250514',");
lines.push("          max_tokens: 4000,");
lines.push("          messages: [{ role: 'user', content: prompt }]");
lines.push("        })");
lines.push("      })");
lines.push("      const data = await response.json()");
lines.push("      const text = data.content[0].text");
lines.push("      const parsed = JSON.parse(text)");
lines.push("      setPost(parsed)");
lines.push("    } catch (e) {");
lines.push("      setError('Generation failed. Please try again.')");
lines.push("    }");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  async function savePost() {");
lines.push("    if (!post) return");
lines.push("    try {");
lines.push("      const { data: { user } } = await supabase.auth.getUser()");
lines.push("      await supabase.from('blog_posts').insert({");
lines.push("        user_id: user.id,");
lines.push("        title: post.title,");
lines.push("        meta_description: post.metaDescription,");
lines.push("        content: post.content,");
lines.push("        topic: topic,");
lines.push("        tone: tone,");
lines.push("        word_count: post.wordCount,");
lines.push("        status: 'draft',");
lines.push("        created_at: new Date().toISOString()");
lines.push("      })");
lines.push("      setSaved(true)");
lines.push("    } catch (e) {");
lines.push("      setError('Save failed. Please try again.')");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  function copyPost() {");
lines.push("    if (!post) return");
lines.push("    navigator.clipboard.writeText(post.content)");
lines.push("    setCopied(true)");
lines.push("    setTimeout(() => setCopied(false), 2000)");
lines.push("  }");
lines.push("");
lines.push("  return (");
lines.push("    <>");
lines.push("      <Nav />");
lines.push("      <section style={{ background: '#111', color: '#fff', textAlign: 'center', padding: '60px 32px' }}>");
lines.push("        <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '13px', fontWeight: 600, letterSpacing: '2px', color: '#E8610A', textTransform: 'uppercase', marginBottom: '12px' }}>AI Blog Generator</p>");
lines.push("        <h1 style={{ fontFamily: 'Playfair Display, serif', fontSize: '48px', fontWeight: 900, lineHeight: 1.1, margin: '0 auto 16px' }}>Generate a Blog Post</h1>");
lines.push("        <p style={{ fontSize: '17px', color: '#ccc', maxWidth: '560px', margin: '0 auto' }}>SEO-optimized and AI engine-ready. Every post is built to rank on Google and get cited by ChatGPT, Perplexity, and Gemini.</p>");
lines.push("      </section>");
lines.push("");
lines.push("      <section style={{ background: '#fff', padding: '60px 32px', maxWidth: '800px', margin: '0 auto' }}>");
lines.push("        <div style={{ marginBottom: '24px' }}>");
lines.push("          <label style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '15px', fontWeight: 600, display: 'block', marginBottom: '8px' }}>Topic or Keyword</label>");
lines.push("          <input type='text' placeholder='e.g. Best HVAC tips for summer in Fort Pierce' value={topic} onChange={e => setTopic(e.target.value)} style={{ width: '100%', padding: '14px 16px', fontSize: '16px', border: '2.5px solid #111', outline: 'none', fontFamily: 'DM Sans, sans-serif', boxSizing: 'border-box' }} />");
lines.push("        </div>");
lines.push("");
lines.push("        <div style={{ marginBottom: '32px' }}>");
lines.push("          <label style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '15px', fontWeight: 600, display: 'block', marginBottom: '8px' }}>Tone</label>");
lines.push("          <select value={tone} onChange={e => setTone(e.target.value)} style={{ width: '100%', padding: '14px 16px', fontSize: '16px', border: '2.5px solid #111', outline: 'none', fontFamily: 'DM Sans, sans-serif', boxSizing: 'border-box', background: '#fff' }}>");
lines.push("            <option>Professional</option>");
lines.push("            <option>Friendly</option>");
lines.push("            <option>Authoritative</option>");
lines.push("            <option>Conversational</option>");
lines.push("          </select>");
lines.push("        </div>");
lines.push("");
lines.push("        {error && <p style={{ color: 'red', marginBottom: '16px', fontFamily: 'DM Sans, sans-serif' }}>{error}</p>}");
lines.push("");
lines.push("        <button onClick={generatePost} disabled={loading} style={{ width: '100%', background: loading ? '#ccc' : '#E8610A', color: '#fff', padding: '18px', fontSize: '17px', fontWeight: 700, border: 'none', cursor: loading ? 'not-allowed' : 'pointer', fontFamily: 'DM Sans, sans-serif', marginBottom: '40px' }}>");
lines.push("          {loading ? 'Generating your post...' : 'Generate Blog Post'}");
lines.push("        </button>");
lines.push("");
lines.push("        {loading && (");
lines.push("          <div style={{ textAlign: 'center', padding: '40px', background: '#f9f9f9', border: '2px solid #eee', marginBottom: '32px' }}>");
lines.push("            <p style={{ fontFamily: 'Playfair Display, serif', fontSize: '22px', color: '#111', marginBottom: '8px' }}>Writing your post...</p>");
lines.push("            <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '14px', color: '#888' }}>Optimizing for Google, ChatGPT, Perplexity and Gemini. This takes about 15 seconds.</p>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {post && (");
lines.push("          <div style={{ border: '2.5px solid #111', borderRadius: '8px', overflow: 'hidden' }}>");
lines.push("            <div style={{ background: '#111', padding: '20px 24px', display: 'flex', alignItems: 'center', justifyContent: 'space-between', flexWrap: 'wrap', gap: '12px' }}>");
lines.push("              <div>");
lines.push("                <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '12px', color: '#E8610A', fontWeight: 700, textTransform: 'uppercase', letterSpacing: '1px', marginBottom: '4px' }}>Generated Post</p>");
lines.push("                <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '13px', color: '#aaa' }}>{post.wordCount} words</p>");
lines.push("              </div>");
lines.push("              <div style={{ display: 'flex', gap: '10px' }}>");
lines.push("                <button onClick={copyPost} style={{ background: 'transparent', border: '1.5px solid #666', color: '#fff', padding: '8px 16px', fontSize: '13px', fontWeight: 600, cursor: 'pointer', fontFamily: 'DM Sans, sans-serif', borderRadius: '4px' }}>");
lines.push("                  {copied ? 'Copied!' : 'Copy HTML'}");
lines.push("                </button>");
lines.push("                <button onClick={savePost} disabled={saved} style={{ background: saved ? '#2d6a2d' : '#E8610A', border: 'none', color: '#fff', padding: '8px 16px', fontSize: '13px', fontWeight: 600, cursor: saved ? 'default' : 'pointer', fontFamily: 'DM Sans, sans-serif', borderRadius: '4px' }}>");
lines.push("                  {saved ? 'Saved!' : 'Save Post'}");
lines.push("                </button>");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            <div style={{ padding: '24px', background: '#fafafa', borderBottom: '1.5px solid #eee' }}>");
lines.push("              <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '11px', fontWeight: 700, color: '#E8610A', textTransform: 'uppercase', letterSpacing: '1px', marginBottom: '4px' }}>SEO Title</p>");
lines.push("              <p style={{ fontFamily: 'Playfair Display, serif', fontSize: '20px', color: '#111', fontWeight: 700 }}>{post.title}</p>");
lines.push("            </div>");
lines.push("            <div style={{ padding: '24px', background: '#fafafa', borderBottom: '1.5px solid #eee' }}>");
lines.push("              <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '11px', fontWeight: 700, color: '#E8610A', textTransform: 'uppercase', letterSpacing: '1px', marginBottom: '4px' }}>Meta Description</p>");
lines.push("              <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '14px', color: '#444' }}>{post.metaDescription}</p>");
lines.push("            </div>");
lines.push("            <div style={{ padding: '32px', background: '#fff' }} dangerouslySetInnerHTML={{ __html: post.content }} />");
lines.push("          </div>");
lines.push("        )}");
lines.push("      </section>");
lines.push("      <Footer />");
lines.push("    </>");
lines.push("  )");
lines.push("}");

fs.writeFileSync(path.join(dir, 'page.tsx'), lines.join('\n'), 'utf8');
console.log('SUCCESS: Blog generator page created.');