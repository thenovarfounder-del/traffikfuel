const fs = require('fs');
const path = require('path');

const apiDir = path.join(__dirname, 'src', 'app', 'api', 'generate-blog');
fs.mkdirSync(apiDir, { recursive: true });

const route = [];
route.push("// @ts-nocheck");
route.push("");
route.push("export async function POST(request) {");
route.push("  try {");
route.push("    const { topic, tone, businessName, industry, city } = await request.json()");
route.push("");
route.push("    const prompt = 'Write a blog post for a ' + industry + ' business called ' + businessName + ' in ' + city + '. Topic: ' + topic + '. Tone: ' + tone + '.'");
route.push("      + ' Include: SEO title, meta description under 160 chars, intro mentioning ' + city + ', 4 H2 sections with keywords, FAQ with 3 questions, strong CTA mentioning ' + businessName + '.'");
route.push("      + ' Optimize so ChatGPT and Perplexity cite this business for ' + industry + ' in ' + city + '.'");
route.push("      + ' Return ONLY a JSON object with fields: title, metaDescription, content (full HTML), wordCount. No markdown. No backticks.'");
route.push("");
route.push("    const response = await fetch('https://api.anthropic.com/v1/messages', {");
route.push("      method: 'POST',");
route.push("      headers: {");
route.push("        'Content-Type': 'application/json',");
route.push("        'x-api-key': process.env.ANTHROPIC_API_KEY,");
route.push("        'anthropic-version': '2023-06-01'");
route.push("      },");
route.push("      body: JSON.stringify({");
route.push("        model: 'claude-opus-4-5',");
route.push("        max_tokens: 4000,");
route.push("        messages: [{ role: 'user', content: prompt }]");
route.push("      })");
route.push("    })");
route.push("");
route.push("    const data = await response.json()");
route.push("");
route.push("    if (!response.ok) {");
route.push("      return Response.json({ success: false, error: JSON.stringify(data) }, { status: 500 })");
route.push("    }");
route.push("");
route.push("    if (!data.content || !data.content[0]) {");
route.push("      return Response.json({ success: false, error: 'No content returned: ' + JSON.stringify(data) }, { status: 500 })");
route.push("    }");
route.push("");
route.push("    const text = data.content[0].text");
route.push("    const clean = text.replace(/```json|```/g, '').trim()");
route.push("    const parsed = JSON.parse(clean)");
route.push("");
route.push("    return Response.json({ success: true, post: parsed })");
route.push("  } catch (e) {");
route.push("    return Response.json({ success: false, error: e.message }, { status: 500 })");
route.push("  }");
route.push("}");

fs.writeFileSync(path.join(apiDir, 'route.ts'), route.join('\n'), 'utf8');
console.log('SUCCESS: API route updated with better error handling.');