route.push("      + ' Return ONLY a JSON object with these exact fields: title, metaDescription, content (full HTML), wordCount, readingTime (number in minutes), tags (array of strings), relatedTopics (array of 3 strings), socialCaption (string), schema (JSON-LD schema markup as a string).'");
route.push("      + ' No markdown. No backticks. No explanation.'");
route.push("");
route.push("    const response = await fetch('https://api.anthropic.com/v1/messages', {");
route.push("      method: 'POST',");
route.push("      headers: {");
route.push("        'Content-Type': 'application/json',");
route.push("        'x-api-key': process.env.ANTHROPIC_API_KEY,");
route.push("        'anthropic-version': '2023-06-01'");
route.push("      },");
route.push("      body: JSON.stringify({");
route.push("        model: 'claude-opus-4-5',");
route.push("        max_tokens: 5000,");
route.push("        messages: [{ role: 'user', content: prompt }]");
route.push("      })");
route.push("    })");
route.push("");
route.push("    const data = await response.json()");
route.push("    if (!response.ok) return Response.json({ success: false, error: JSON.stringify(data) }, { status: 500 })");
route.push("    if (!data.content || !data.content[0]) return Response.json({ success: false, error: 'No content: ' + JSON.stringify(data) }, { status: 500 })");
route.push("");
route.push("    const text = data.content[0].text");
route.push("    const clean = text.replace(/```json|```/g, '').trim()");
route.push("    const parsed = JSON.parse(clean)");
route.push("    return Response.json({ success: true, post: parsed })");
route.push("  } catch (e) {");
route.push("    return Response.json({ success: false, error: e.message }, { status: 500 })");
route.push("  }");
route.push("}");

