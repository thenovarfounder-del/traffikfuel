lines.push("          industry: industry,");
lines.push("          website: website,");
lines.push("          phone: city");
lines.push("        }, { onConflict: 'user_id' })");
lines.push("      if (upsertError) { setError('Save failed: ' + upsertError.message); setLoading(false); return }");
lines.push("      router.push('/dashboard')");
lines.push("    } catch (e) {");
lines.push("      setError('Something went wrong. Please try again.')");
lines.push("      setLoading(false)");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  return (");
lines.push("    <>");
lines.push("      <Nav />");
lines.push("      <section style={{ background: '#111', color: '#fff', textAlign: 'center', padding: '90px 32px' }}>");
lines.push("        <p style={{ fontFamily: 'DM Sans, sans-serif', fontSize: '13px', fontWeight: 600, letterSpacing: '2px', color: '#E8610A', textTransform: 'uppercase', marginBottom: '16px' }}>Business Setup</p>");
lines.push("        <h1 style={{ fontFamily: 'Playfair Display, serif', fontSize: '54px', fontWeight: 900, lineHeight: 1.1, maxWidth: '820px', margin: '0 auto 24px' }}>Tell Us About Your Business</h1>");
